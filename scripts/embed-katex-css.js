// Author-time generator (run once, output committed). Reads KaTeX's dist
// stylesheet and rewrites every @font-face to a single base64-embedded woff2
// data URI, dropping the legacy woff/ttf `src` entries. Emits
// src/render/katex/katexCss.ts exporting KATEX_CSS as a plain string so the
// render layer ships fully self-contained (zero network fetches at print time
// — required because expo-print's Android WebView has no network at view time).
//
// Unlike the spike (which embedded only the 12 font families its torture doc
// used), this embeds the FULL KaTeX woff2 set (all 20 families) so ANY math a
// real chat answer contains renders — see spike/FINDINGS.md gotcha #2.
//
// Regenerate:  node scripts/embed-katex-css.js
const fs = require('fs');
const path = require('path');

const DIST = path.join(__dirname, '..', 'node_modules', 'katex', 'dist');
const FONTS = path.join(DIST, 'fonts');
const OUT = path.join(__dirname, '..', 'src', 'render', 'katex', 'katexCss.ts');

let css = fs.readFileSync(path.join(DIST, 'katex.min.css'), 'utf8');

let embedded = 0;
css = css.replace(/@font-face\{[^}]*\}/g, (block) => {
  const m = block.match(/url\(fonts\/([A-Za-z0-9_-]+)\.woff2\)/);
  if (!m) return block; // no woff2 variant — leave block untouched
  const b64 = fs.readFileSync(path.join(FONTS, m[1] + '.woff2')).toString('base64');
  embedded++;
  // Replace the whole comma-separated `src:` list (woff2, woff, ttf) with one
  // embedded woff2 data URI. woff2 is universally supported by the Android
  // System WebView versions expo-print targets; woff/ttf are dead weight.
  return block.replace(/src:[^}]+/, `src:url(data:font/woff2;base64,${b64}) format("woff2")`);
});

// ---- Fail closed: every @font-face must have been embedded and no on-disk
// font reference may survive (a single url(fonts/...) left behind = blank math
// glyphs on-device, silently). Exit non-zero so CI/author catches it.
const totalFontFaces = (css.match(/@font-face\{/g) || []).length;
if (embedded !== totalFontFaces) {
  console.error(
    `FAIL: embedded ${embedded} of ${totalFontFaces} @font-face blocks — ` +
      'some faces have no woff2 variant or the rewrite regex missed them.',
  );
  process.exit(1);
}
if (/url\(fonts\//.test(css)) {
  console.error('FAIL: un-embedded url(fonts/...) references remain in the output CSS.');
  process.exit(1);
}

const header =
  '// AUTO-GENERATED by scripts/embed-katex-css.js — DO NOT EDIT BY HAND.\n' +
  '// Full KaTeX stylesheet with all woff2 font families embedded as base64\n' +
  '// data URIs (legacy woff/ttf dropped). Regenerate: node scripts/embed-katex-css.js\n';
const body = 'export const KATEX_CSS: string = ' + JSON.stringify(css) + ';\n';

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, header + body);

const kb = (fs.statSync(OUT).size / 1024).toFixed(1);
console.error(`fonts embedded: ${embedded}; katexCss.ts written: ${kb} KB`);
