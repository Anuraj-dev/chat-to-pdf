// Author-time generator (run once, output committed). Base64-embeds the
// JetBrains Mono Latin-subset woff2 files into @font-face rules and emits
// src/render/codeFont/codeFontCss.ts exporting CODE_FONT_CSS as a plain string.
//
// Mirrors scripts/embed-katex-css.js: the render layer must ship fully
// self-contained because expo-print's Android WebView has no network at print
// time. JetBrains Mono was chosen for code legibility (dotted zero, distinct
// 1/l/I, wide operators) so a non-technical reader never mistypes a printed
// command. SIL OFL 1.1 — free to embed. Regenerate: node scripts/embed-code-font.js
const fs = require('fs');
const path = require('path');

// Regular + Bold + Italic + Bold-Italic: the syntax-highlight theme uses bold
// (keywords/numbers) and italic (comments), so real weights beat faux-synthesized
// ones. Latin subset only (~4×22KB) — trivial next to the embedded KaTeX set.
const FILES = [
  { file: 'jetbrains-mono-latin-400-normal.woff2', weight: 400, style: 'normal' },
  { file: 'jetbrains-mono-latin-700-normal.woff2', weight: 700, style: 'normal' },
  { file: 'jetbrains-mono-latin-400-italic.woff2', weight: 400, style: 'italic' },
  { file: 'jetbrains-mono-latin-700-italic.woff2', weight: 700, style: 'italic' },
];
const DIR = path.join(__dirname, '..', 'node_modules', '@fontsource', 'jetbrains-mono', 'files');
const OUT = path.join(__dirname, '..', 'src', 'render', 'codeFont', 'codeFontCss.ts');

let css = '';
for (const { file, weight, style } of FILES) {
  const abs = path.join(DIR, file);
  if (!fs.existsSync(abs)) {
    console.error(`FAIL: missing font file ${file} — is @fontsource/jetbrains-mono installed?`);
    process.exit(1);
  }
  const b64 = fs.readFileSync(abs).toString('base64');
  css +=
    `@font-face{font-family:"JetBrains Mono";font-style:${style};font-weight:${weight};` +
    `font-display:swap;src:url(data:font/woff2;base64,${b64}) format("woff2");}\n`;
}

// Fail closed: every requested face must be embedded and no on-disk reference
// may survive (a stray url(...) = blank glyphs on-device, silently).
if ((css.match(/@font-face\{/g) || []).length !== FILES.length) {
  console.error('FAIL: not every @font-face was embedded.');
  process.exit(1);
}

const header =
  '// AUTO-GENERATED by scripts/embed-code-font.js — DO NOT EDIT BY HAND.\n' +
  '// JetBrains Mono (Latin subset, woff2) embedded as base64 data URIs for the\n' +
  '// code-block font. SIL OFL 1.1. Regenerate: node scripts/embed-code-font.js\n';
const body = 'export const CODE_FONT_CSS: string = ' + JSON.stringify(css) + ';\n';

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, header + body);

const kb = (fs.statSync(OUT).size / 1024).toFixed(1);
console.error(`code font faces embedded: ${FILES.length}; codeFontCss.ts written: ${kb} KB`);
